

# MAPEAR : es aplicarle una operacion a cada elemento de una lista. Es como hacer una PROYECCION, agarro 2 valores de un diccionario y lo guardo en otro lado. Deja de ser de la lista original del diccionario, y lo preparo en otra lista para mostrar por consola

# MAPEAR UNA LISTA: es crear una lista nueva con la misma cantidad de elementos de los originales, pero procesando cada uno de esos elementos

# FILTRAR UNA LISTA: es tener una lista nueva donde solamente estan los elementos de la lista original que cumplen con una determinada condicion

# Se puede mezclar entre mapear y filtrar

